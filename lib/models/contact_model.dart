class Contact { // Modèle représentant un contact
  String name; // Nom du contact
  String email; // Adresse e-mail du contact
  String phone; // Numéro de téléphone du contact
  Contact( // Constructeur de la classe Contact
      {required this.name, required this.email, required this.phone} // Paramètres requis: name, email, phone
  ); // Fin du constructeur
} // Fin de la classe Contact